/* Home page events */

function arrowHoverOffEvent(name) {
    var arrow = document.getElementById(name);

    arrow.style.position = "absolute";
    arrow.style.left = "0px";
}

function arrowHoverEvent(name) {
    var arrow = document.getElementById(name);

    arrow.style.position = "absolute";
    arrow.style.left = "100px";
}